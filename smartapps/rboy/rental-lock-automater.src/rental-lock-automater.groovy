                msgs << msg
                log.warn(msg + "\n$e")
            }
        }
    }

    log.trace "Found ${entries.size()} Booking.com schedules, processed ${details.size()} entries for ${propertyName ? "property ${propertyName}" : "unit ${no}"}"

    // Save it
    atomicState."unit${no}Booking" = details
    atomicState."unit${no}BookingUpdated" = now()

    // Do this in the end otherwise it may timeout
    def tZ = (rentalProgrammingNotificationsStart && rentalProgrammingNotificationsEnd) ? getPhoneTimeZone(rentalProgrammingNotificationsStart) : null // Only push if we are within the specified time limits for the Mobile phone timezone
    msgs.each { msg ->
        if (rentalProgrammingNotifications &&
            (tZ ? timeOfDayIsBetween(timeToday(rentalProgrammingNotificationsStart, tZ), timeToday(rentalProgrammingNotificationsEnd, tZ), new Date(now()), tZ) : true)
