    def suffix = data?.suffix // Unit suffix
    def propertyName = no ? (settings."propertyName${unitSuffixes[no]}" ?: "") : "" // Don't use null

    if (ret.hasError()) {
        def msg
        if ([ 301, 302, 303, 304, 305, 306, 307, 308 ].any { status -> status == ret.status } && ret.headers.Location) { // Sometimes ST doesn't handle redirect propertly, so let us handle it
            msg = "VRBO response from ${propertyName ? "property ${propertyName}" : "unit ${no}"} redirected to: ${ret.headers.Location}. Retrying redirect"
            log.warn msg
            if (settings."iCalURLVRBO${suffix}" == ret.headers.Location) { // We shouldn't be stuck in a loop
                msg = "Error downloading ${propertyName ? "property ${propertyName}" : "unit ${no}"} VRBO calendar, redirect loop. Retrying in about ${rentalUpdateFrequency} minutes"
                log.error msg
            } else if (!downloadiCalendar(ret.headers.Location, [pinLen: pinLen, no: no, suffix: suffix], processVRBOResponse)) { // Download the calendar
                msg = "Error downloading ${propertyName ? "property ${propertyName}" : "unit ${no}"} VRBO calendar redirect. Retrying in about ${rentalUpdateFrequency} minutes"
                log.error msg
            } else {
                return // We're done here
            }
        } else {            
            msg = "VRBO response from ${propertyName ? "property ${propertyName}" : "unit ${no}"} returned an error: ${ret.getErrorMessage()}. Retrying in about ${rentalUpdateFrequency} minutes"
            log.error (msg + "\nException while querying: status ${ret.status}, warning ${ret.warningMessages}, error ${ret.errorData}, headers=${ret.headers}")
        }
        
        def tZ = (rentalProgrammingNotificationsStart && rentalProgrammingNotificationsEnd) ? getPhoneTimeZone(rentalProgrammingNotificationsStart) : null // Only push if we are within the specified time limits for the Mobile phone timezone
        if (rentalProgrammingNotifications &&
            (tZ ? timeOfDayIsBetween(timeToday(rentalProgrammingNotificationsStart, tZ), timeToday(rentalProgrammingNotificationsEnd, tZ), new Date(now()), tZ) : true)
           ) {
            sendNotifications(msg)
        } else {
            sendNotificationEvent(msg)
        }
        return
    }
    
    log.trace "Received VRBO response from ${propertyName ? "property ${propertyName}" : "unit ${no}"}, status=${ret.status}, warning=${ret.warningMessages}, params=$data, json=${ret.json?.size()} characters, rawData:${ret.data?.size()} characters, headers=${ret.headers?.size()} fields"

    def msgs = []
    def iCal = ret.data // Raw iCalendar data
    
    if (pinLen > maxAutoPinLen) {
        def msg = "Maximum pin length allowed is 8 digits, lock configured to accept ${pinLen} digits"
        log.error msg
        sendNotifications(msg) // Last thing to avoid timeout
        return // We're done here
    }

    if (!iCal?.trim()) { // If it's empty something is wrong
        def msg = "No reservation data available in VRBO response from ${propertyName ? "property ${propertyName}" : "unit ${no}"}"
        log.error msg
        def tZ = (rentalProgrammingNotificationsStart && rentalProgrammingNotificationsEnd) ? getPhoneTimeZone(rentalProgrammingNotificationsStart) : null // Only push if we are within the specified time limits for the Mobile phone timezone
        if (rentalProgrammingNotifications &&
            (tZ ? timeOfDayIsBetween(timeToday(rentalProgrammingNotificationsStart, tZ), timeToday(rentalProgrammingNotificationsEnd, tZ), new Date(now()), tZ) : true)
           ) {
            sendNotifications(msg)
        } else {
            sendNotificationEvent(msg)
        }
        return // We're done here - special case it failed
    }

    if (!autoCodeVRBO) {
        def msg = "VRBO automatic code generation format not defined in Property Settings"
        log.error msg
        sendNotifications(msg) // Last thing to avoid timeout
        return // We're done here
    }

    TimeZone timeZone = location.timeZone
    if (!timeZone) {
        timeZone = TimeZone.getDefault()
        log.error "Hub location/timezone not set, using ${timeZone.getDisplayName()} timezone. Please set Hub location and timezone for the codes to work accurately"
        sendPush "Hub location/timezone not set, using ${timeZone.getDisplayName()} timezone. Please set Hub location and timezone for the codes to work accurately"
    }

    //log.trace "The date/time on the hub now is ${(new Date(now())).format("EEE MMM dd yyyy HH:mm z", timeZone)}"

    // Process the calendar into usable entries
    def entries = iCalToMapListVRBO(iCal)

    def oldEntries = atomicState."unit${no}Vrbo" ?: [] // Get the last update to check if reservation dates have been updated
    def details = []
    // Find the entry for today
    for (entry in entries) {
        // Details about users checkin, checkout, phone and name
        def name = entry.SUMMARY?.trim()
        if (name.startsWith("Reserved - ")) { // Only take confirmed reservations to schedule
            name = name.replace("Reserved - ", "")?.trim() + (addReservationIdToName ? " (" + entry.UID?.take(8) + ")" : "") // Remove the header and keep the name and add the UID to uniquely identify it (repeat renters)

            // Dates are in the format YYYYMMDD
            def startDate = entry['DTSTART']?.trim()?.substring(0, 8)
            def startTime = checkInTime
            def expDate = entry['DTEND']?.trim()?.substring(0, 8)
            def expTime = checkOutTime
            //def userLocks = (settings."propertyLocks${suffix}" ? (locks*.id?.every { settings."propertyLocks${suffix}"?.contains(it) } ? "" : settings."propertyLocks${suffix}") : "") // Only save the user locks if they aren't all (to save state variable space in ST has a paltry 100K space)

            def code = generateAutomaticCode(autoCodeVRBO, pinLen, "", startDate, expDate)
            if (code == null) {
                def msg = "VRBO unknown automatic code generation format: $autoCodeVRBO"
                log.error msg
                sendNotifications(msg) // Last thing to avoid timeout
                return // We're done here
            }

            //log.trace "VRBO Rental entry: $name, code: $code, startDate: $startDate, startTime: $startTime, expDate: $expDate, expTime: $expTime, unit: $no"

            try { // Verify that we have valid date/times and check if any changes in reservation dates
                def exp = Date.parse("yyyyMMddHH:mmZ", expDate + timeToday(expTime, timeZone).format("HH:mmZ", timeZone))
                def start = Date.parse("yyyyMMddHH:mmZ", startDate + timeToday(expTime, timeZone).format("HH:mmZ", timeZone))
                def oldEntry = oldEntries?.find { it.name == name } // Match by name
                def updatedStartDate = ((oldEntry && startDate) ? (oldEntry.startDate != startDate) : false)
                def updatedExpDate = ((oldEntry && expDate) ? (oldEntry.expDate != expDate) : false)
                if ((updatedStartDate || updatedExpDate) && startDate && expDate) { // If we have a valid entry notify user
                    // Sometimes old calendar entries are updated, so ignore them and only check for reservations who's end date is today or afterwards
                    def oldExp = Date.parse("yyyyMMddHH:mmZ", (oldEntry.expDate ?: expDate) + timeToday(expTime, timeZone).format("HH:mmZ", timeZone)) // If old doesn't exist, use current (since we could be in here from an updated start date)
                    if ((oldExp >= new Date(now())) || (exp >= new Date(now()))) { // If the old or now expiration is in future (i.e. active or to become active)
                        def dtMsg = ""
                        if (updatedStartDate) {
                            def oldStart = Date.parse("yyyyMMddHH:mmZ", oldEntry.startDate + timeToday(expTime, timeZone).format("HH:mmZ", timeZone))
                            def startStr = start.format("MMM dd", timeZone)
                            def oldStartStr = oldStart.format("MMM dd", timeZone)
                            dtMsg += "start date changed from ${oldStartStr} to ${startStr}"
                        }

                        if (updatedExpDate) {
                            def expStr = exp.format("MMM dd", timeZone)
                            def oldExpStr = oldExp.format("MMM dd", timeZone)
                            dtMsg += (updatedStartDate ? " and " : "") + "end date changed from ${oldExpStr} to ${expStr}"
                        }

                        def msg = "VRBO reservation ${dtMsg} for ${name} in ${propertyName ? "property ${propertyName}" : "unit ${no}"}"
                        msgs << msg
                        log.trace msg
                    }
                }
                
                // Save it only if expiration is in future or the date has changed and upto the max rental users entry limit (limit amount of data being saved since ST's state limit is a paltry 100K)
                if ((updatedStartDate || updatedExpDate || (exp >= new Date(now()))) &&
                	(details.size() <= (maxRentalUsers + (details.findAll { it.updatedStartDate || it.updatedExpDate }.size())))) { // Adjust for any extra entries we are keeping due to updated start/exp dates beyond the max rental users
                    details << [ name: name, code: code, startDate: startDate, updatedStartDate: updatedStartDate, expDate: expDate, updatedExpDate: updatedExpDate, unit: no, bookingSource: "VRBO" ] // Don't have startTime, expTime and userLocks to save memory
                }
            } catch (Exception e) { // Skip invalid entries but inform the user
                def msg = "Skipping invalid entry in VRBO ${propertyName ? "property ${propertyName}" : "unit ${no}"} for ${name} starting ${startDate}, ending ${expDate}"
                msgs << msg
                log.warn(msg + "\n$e")
            }
        }
    }

    log.trace "Found ${entries.size()} VRBO schedules, processed ${details.size()} entries for ${propertyName ? "property ${propertyName}" : "unit ${no}"}"

    // Save it
    atomicState."unit${no}Vrbo" = details
    atomicState."unit${no}VrboUpdated" = now()

    // Do this in the end otherwise it may timeout
    def tZ = (rentalProgrammingNotificationsStart && rentalProgrammingNotificationsEnd) ? getPhoneTimeZone(rentalProgrammingNotificationsStart) : null // Only push if we are within the specified time limits for the Mobile phone timezone
    msgs.each { msg ->
        if (rentalProgrammingNotifications &&
            (tZ ? timeOfDayIsBetween(timeToday(rentalProgrammingNotificationsStart, tZ), timeToday(rentalProgrammingNotificationsEnd, tZ), new Date(now()), tZ) : true)
           ) {
            sendNotifications(msg)
        } else {
            sendNotificationEvent(msg)
        }
    }
}

