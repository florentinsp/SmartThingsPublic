                            def oldStart = Date.parse("yyyyMMddHH:mmZ", oldEntry.startDate + timeToday(expTime, timeZone).format("HH:mmZ", timeZone))
                            def startStr = start.format("MMM dd", timeZone)
                            def oldStartStr = oldStart.format("MMM dd", timeZone)
                            dtMsg += "start date changed from ${oldStartStr} to ${startStr}"
                        }

                        if (updatedExpDate) {
                            def expStr = exp.format("MMM dd", timeZone)
                            def oldExpStr = oldExp.format("MMM dd", timeZone)
                            dtMsg += (updatedStartDate ? " and " : "") + "end date changed from ${oldExpStr} to ${expStr}"
                        }

                        def msg = "Booking.com reservation ${dtMsg} for ${name} in ${propertyName ? "property ${propertyName}" : "unit ${no}"}"
                        msgs << msg
                        log.trace msg
                    }
                }
                
                // Save it only if expiration is in future or the date has changed and upto the max rental users entry limit (limit amount of data being saved since ST's state limit is a paltry 100K)
                if ((updatedStartDate || updatedExpDate || (exp >= new Date(now()))) &&
                	(details.size() <= (maxRentalUsers + (details.findAll { it.updatedStartDate || it.updatedExpDate }.size())))) { // Adjust for any extra entries we are keeping due to updated start/exp dates beyond the max rental users
                    details << [ name: name, code: code, startDate: startDate, updatedStartDate: updatedStartDate, expDate: expDate, updatedExpDate: updatedExpDate, unit: no, bookingSource: "Booking" ] // Don't have startTime, expTime and userLocks to save memory
